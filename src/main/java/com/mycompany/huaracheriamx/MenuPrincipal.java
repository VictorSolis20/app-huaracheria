    //M√©todo para obtener la fecha actual del sistema
    private void SetDate() {
        LocalDate now = LocalDate.now();
        Locale spanishLocale = new Locale("es", "ES");
        fechaLbl.setText(now.format(DateTimeFormatter.ofPattern("'Hoy es ' EEEE dd 'de' MMMM 'de' yyyy", spanishLocale)));
    }

    //M√©todo para obtener la hora actual del sistema
    private void SetHora() {
        // Configurar la zona horaria de M√©xico
        ZoneId zonaMexico = ZoneId.of("America/Mexico_City");

        // Actualizar la hora cada segundo
        Timer timer = new Timer(1000, e -> {
            LocalTime horaActual = LocalTime.now(zonaMexico);
            String horaFormateada = horaActual.format(DateTimeFormatter.ofPattern("hh:mm:ss a"));
            recibeHoraLbl.setText(horaFormateada);
        });
        timer.start();
    }

