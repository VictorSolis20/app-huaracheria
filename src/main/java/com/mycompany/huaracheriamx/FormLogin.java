    private void ingresarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ingresarBtnActionPerformed
        //Se extraen los datos ingresados por el usuario mediante la interfaz grafica
        String usuario = usuarioTxt.getText();
        //Se extraen los caracteres del password y se guardan en una variable de tipo char[]
        char[] contrasena = passwordTxt.getPassword();

        if (usuario.isEmpty() || contrasena.length == 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe llenar todos los campos \n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
            // Posiciona el cursor en el campo estadoTxt
            usuarioTxt.requestFocus();
            return;
        }
        DAOLoginImpl objetoLogin = new DAOLoginImpl();
        try {
            objetoLogin.validaUsuario(usuarioTxt, passwordTxt);
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_ingresarBtnActionPerformed

