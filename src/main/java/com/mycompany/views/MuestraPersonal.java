    private void editarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editarBtnActionPerformed
        if (jTable1.getSelectedRow() > -1) {
            try {
                int personalId = (int) jTable1.getValueAt(jTable1.getSelectedRow(), 0);
                DAOPersonal dao = new DAOPersonalImpl();
                MenuPrincipal.ShowJPanel(new EditarPersonal(dao.getPersonalById(personalId)));
            } catch (Exception e) {
                System.out.println(e.getMessage());
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Debes seleccionar el trabajador a editar. \n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_editarBtnActionPerformed

    private void domicilioBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_domicilioBtnActionPerformed
        if (jTable1.getSelectedRow() > -1) {
            int personalSeleccionadoId = (int) jTable1.getValueAt(jTable1.getSelectedRow(), 0);
            MenuPrincipal.ShowJPanel(new MuestraDomicilioPersonal(personalSeleccionadoId));
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe seleccionar el trabajador del cual va a consultar sus domicilios. \n", "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_domicilioBtnActionPerformed


